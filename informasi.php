<div class="title">Informasi Sistem Pakar </div>
<br />
<b>Sistem pakar</b> (<i>expert system</i>) adalah sistem yang berusaha mengapdosi pengetahuan manusia ke komputer, agar komputer dapat menyelesaikan masalah seperti yang biasa dilakukan oleh para ahli. Sistem pakar yang baik dirancang agar dapat menyelesaikan suatu permasalahan tertentu dengan meniru kerja dari para ahli.
<br>
<br />
<b>Sistem Pakar identifikasi VERTEBRATA </b> adalah sarana untuk melakukan identifikasi secara komputerisasi berbasis web online sehingga mendapatkan informasi cepat dan tepat. Karena Vertebrata memiliki 5 golongan kelas yaitu Pisces, Amphibia, Reptile, Aves, dan Mamalia.<br />
Sumber basis pengetahuan dari sistem ini diperoleh dari hasil studi pustaka dari berbagai sumber yang berkaitan dengan Basis Pengetahuan Vertebrata.<br />
